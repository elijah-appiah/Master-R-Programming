# Addition
67 + 90

# Subtraction
67 - 90

# Multiplication
67 * 10

# Division
67 / 10

# Exponentiation
6 ** 2
6 ^ 2

# Modulo (returns the remainder after division)
5 %% 2
16 %% 4

# Integer Division (returns the integer after division)
15 %/% 7

# Assign 18 to `x`, and 7 to `y`
x <- 18
y <- 7

x + y      # Addition
x - y      # Subtraction
x * y      # Multiplication
x / y      # Division
x ^ y      # Exponentiation
x %% y     # Modulo
x %/% y    # Integer Division


# Complex Arithmetic Operation
5 + 3 * 2 ^ 4 / 2 - 7

# Acronym:  BODMAS (order of precedence for arithmetic operations) 

# `B` ---- Bracket (parenthesis)
# `O` ---- Order - like exponents, square roots
# `D` ---- Division
# `M` ---- Multiplication
# `A` ---- Addition
# `S` ---- Subtraction

# You can guide arithmetic operations with "Brackets" as top priority
(5 + 3) * (2 ^ (4 / 2) - 7)

